package lesson2;

public class EqualityOperators {
    public static void main(String[] args) {
        // =
        int age = 5;
        System.out.println(age);

        // +=
        age += 10; // age = age + 10
        System.out.println(age);

        // -=
        age -= 4; // age = age - 4
        System.out.println(age);

        // /=
        age /= 2; // age = age / 2
        System.out.println(age);

        // *=
        age *= 3; // age = age * 3
        System.out.println(age);


        // %=
        age %= 2; // age = age % 2
        System.out.println(age);

        // -----------------------

        // &=
        age &= 2; // age = age & 2
        System.out.println(age);
    }
}
